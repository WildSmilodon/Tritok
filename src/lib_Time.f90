!----------------------------------------------------------------------C

      REAL*4 FUNCTION cptime (rvar)

!----------------------------------------------------------------------C
! **********************************************************************
! **                                                                  **
! ** Returns the CPU TIME in seconds                                  **
! **             --  ----                                             **
! **********************************************************************
!
      REAL*4 rvar,tmp
      CALL CPU_TIME(tmp)
      cptime = tmp - rvar

      RETURN
      END
